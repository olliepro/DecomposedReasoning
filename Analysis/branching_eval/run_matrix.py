async def run_scored_doc_with_limit_async(
    *,
    plan: DocExecutionPlan,
    semaphore: asyncio.Semaphore,
    config: BranchingEvalConfig,
    spec: ExperimentSpec,
    client: VllmClient,
    model_name_for_generation: str,
    task_name: str,
    model_id: str,
    resolved_branching: BranchingConfig,
    store: ArtifactStore,
    adapter: LmEvalAdapter,
    events_snapshot: list[Any],
) -> DocRunResult:
    """Run, score, and persist one doc under async semaphore limits.

    Args:
        plan: Doc attempt execution plan.
        semaphore: Shared concurrency limiter.
        config: Root config.
        spec: Experiment spec.
        client: vLLM client.
        model_name_for_generation: Request-time model name.
        task_name: Task name.
        model_id: Model label.
        resolved_branching: Branching config.
        store: Artifact store.
        adapter: lm_eval adapter.
        events_snapshot: Event snapshot captured before this execution wave.

    Returns:
        Finalized document result row.
    """

    doc_record = plan.doc_record
    context = doc_event_context(
        run_id=store.run_id,
        spec=spec,
        doc_id=doc_record.doc_id,
        doc_attempt=plan.doc_attempt,
    )
    resume_state: BranchResumeState | None = None
    if spec.mode == "branching" and plan.resume_in_place:
        resume_state = replay_branching_resume_state(
            events=events_snapshot,
            prompt_text=doc_record.prompt_text,
            run_id=store.run_id,
            doc_id=doc_record.doc_id,
            doc_attempt=plan.doc_attempt,
            task_name=task_name,
            model_id=model_id,
            selector_mode=spec.selector or "baseline",
        )
    if plan.resumed_reason is not None:
        store.append_event(
            context=context,
            event_type="doc_resumed",
            payload={"reason": plan.resumed_reason},
        )
    if not plan.has_started_event:
        store.append_event(
            context=context,
            event_type="doc_started",
            payload={"prompt_char_count": len(doc_record.prompt_text)},
        )
    pre_scored = resume_state.pre_scored_leaves if resume_state is not None else {}
    scored_during_rollout: dict[str, LeafRollout] = {}

    def score_and_log_leaf(leaf: LeafRollout) -> LeafRollout:
        """Score one completed leaf immediately and emit `leaf_scored` event."""

        existing = pre_scored.get(leaf.leaf_id)
        if existing is not None:
            scored_during_rollout[leaf.leaf_id] = existing
            return existing
        scored = score_leaf_and_log_event(
            adapter=adapter,
            doc_payload=doc_record.doc_payload,
            leaf=leaf,
            store=store,
            context=context,
        )
        scored_during_rollout[leaf.leaf_id] = scored
        return scored

    async with semaphore:
        has_async_client = callable(getattr(client, "completions_async", None))
        if has_async_client:
            leaves, tree = await run_doc_rollouts_async(
                config=config,
                spec=spec,
                client=client,
                model_name_for_generation=model_name_for_generation,
                prompt_text=doc_record.prompt_text,
                doc_id=doc_record.doc_id,
                doc_attempt=plan.doc_attempt,
                task_name=task_name,
                model_id=model_id,
                resolved_branching=resolved_branching,
                store=store,
                resume_state=resume_state,
                on_leaf_completed=score_and_log_leaf,
            )
        else:
            leaves, tree = await asyncio.to_thread(
                run_doc_rollouts,
                config=config,
                spec=spec,
                client=client,
                model_name_for_generation=model_name_for_generation,
                prompt_text=doc_record.prompt_text,
                doc_id=doc_record.doc_id,
                doc_attempt=plan.doc_attempt,
                task_name=task_name,
                model_id=model_id,
                resolved_branching=resolved_branching,
                store=store,
                resume_state=resume_state,
                on_leaf_completed=None,
            )
    scored_leaves = finalize_scored_leaves(
        leaves=leaves,
        pre_scored=pre_scored,
        scored_during_rollout=scored_during_rollout,
        adapter=adapter,
        doc_payload=doc_record.doc_payload,
        store=store,
        context=context,
    )
    rollout_metrics = [leaf.task_metrics for leaf in scored_leaves]
    doc_metrics = adapter.aggregate_doc_metrics(rollout_metrics=rollout_metrics)
    if tree is not None:
        tree.leaves = scored_leaves
        diagnostics = build_doc_diagnostics(tree=tree)
    else:
        diagnostics = build_baseline_doc_diagnostics(
            doc_id=doc_record.doc_id,
            leaves=scored_leaves,
        )
    store.append_event(
        context=context,
        event_type="doc_finished",
        payload={
            "status": "completed",
            "mode": spec.mode,
            "leaf_count": len(scored_leaves),
            "leaf_lengths": [leaf.length_tokens_total for leaf in scored_leaves],
            "doc_metrics": doc_metrics,
            "diagnostics": asdict(diagnostics),
        },
    )
    return DocRunResult(
        doc_id=doc_record.doc_id,
        doc_attempt=plan.doc_attempt,
        scored_leaves=scored_leaves,
        doc_metrics=doc_metrics,
        diagnostics=diagnostics,
    )


async def run_docs_with_limit_async(
    *,
    plans: list[DocExecutionPlan],
    max_concurrent_docs: int,
    config: BranchingEvalConfig,
    spec: ExperimentSpec,
    client: VllmClient,
    model_name_for_generation: str,
    task_name: str,
    model_id: str,
    resolved_branching: BranchingConfig,
    store: ArtifactStore,
    adapter: LmEvalAdapter,
    events_snapshot: list[Any],
) -> list[DocRunResult]:
    """Run and score many docs with bounded async doc concurrency.

    Args:
        plans: Ordered doc execution plans.
        max_concurrent_docs: Maximum in-flight docs.
        config: Root config.
        spec: Experiment spec.
        client: vLLM client.
        model_name_for_generation: Request-time model name.
        task_name: Task name.
        model_id: Model label.
        resolved_branching: Branching config.
        store: Artifact store.
        adapter: lm_eval adapter.
        events_snapshot: Event snapshot captured before scheduling docs.

    Returns:
        Ordered per-doc result rows.
    """

    semaphore = asyncio.Semaphore(max_concurrent_docs)
    tasks = [
        asyncio.create_task(
            run_scored_doc_with_limit_async(
                plan=plan,
                semaphore=semaphore,
                config=config,
                spec=spec,
                client=client,
                model_name_for_generation=model_name_for_generation,
                task_name=task_name,
                model_id=model_id,
                resolved_branching=resolved_branching,
                store=store,
                adapter=adapter,
                events_snapshot=events_snapshot,
            )
        )
        for plan in plans
    ]
    return list(await asyncio.gather(*tasks))


def recompute_outputs_from_events(
    *,
    store: ArtifactStore,
    adapter: LmEvalAdapter,
    selector_mode: str,
) -> dict[str, Any]:
    """Recompute aggregate outputs from latest completed doc attempts.

    Args:
        store: Artifact store for this run.
        adapter: lm_eval adapter used for aggregate reduction.
        selector_mode: Selector mode label for aggregate diagnostics.

    Returns:
        Run summary payload with completed-doc and leaf counts.
    """

    latest_finished_events: dict[int, Any] = {}
    for event in store.read_events():
        if event.event_type != "doc_finished":
            continue
        if event.doc_id is None or event.doc_attempt is None:
            continue
        previous = latest_finished_events.get(event.doc_id)
        if previous is None:
            latest_finished_events[event.doc_id] = event
            continue
        if (event.doc_attempt, event.event_index) > (
            previous.doc_attempt,
            previous.event_index,
        ):
            latest_finished_events[event.doc_id] = event
    per_doc_metrics: list[dict[str, Any]] = []
    diagnostics: list[DocDiagnostics] = []
    leaf_lengths: list[int] = []
    total_leaf_count = 0
    for event in latest_finished_events.values():
        payload = event.payload
        metrics_payload = payload.get("doc_metrics", {})
        if isinstance(metrics_payload, dict):
            per_doc_metrics.append(metrics_payload)
        diagnostics_payload = payload.get("diagnostics", {})
        if isinstance(diagnostics_payload, dict):
            diagnostics.append(
                diagnostics_from_payload(
                    payload=diagnostics_payload,
                    doc_id=event.doc_id,
                    selector_mode=selector_mode,
                )
            )
        raw_lengths = payload.get("leaf_lengths", [])
        if isinstance(raw_lengths, list):
            leaf_lengths.extend([int(length) for length in raw_lengths])
            total_leaf_count += len(raw_lengths)
    lm_eval_aggregates = adapter.aggregate_task_metrics(per_doc_metrics=per_doc_metrics)
    store.write_lm_eval_aggregates(payload=lm_eval_aggregates)
    aggregate = (
        aggregate_diagnostics(diagnostics=diagnostics)
        if diagnostics
        else AggregateDiagnostics(
            selector_mode=selector_mode,
            doc_count=0,
            mean_bp1_verification_unweighted=0.0,
            mean_bp1_verification_weighted=0.0,
            mean_bp2_verification_unweighted=0.0,
            mean_bp2_verification_weighted=0.0,
            mean_bp1_length_unweighted=0.0,
            mean_bp1_length_weighted=0.0,
            mean_bp2_length_unweighted=0.0,
            mean_bp2_length_weighted=0.0,
        )
    )
    store.write_aggregate_diagnostics(diagnostics=aggregate)
    store.write_length_diagnostics(
        payload=build_length_diagnostics(lengths=leaf_lengths)
    )
    return {
        "doc_count_completed": len(latest_finished_events),
        "leaf_count_completed": total_leaf_count,
    }


def score_leaf_and_log_event(
    *,
    adapter: LmEvalAdapter,
    doc_payload: dict[str, Any],
    leaf: LeafRollout,
    store: ArtifactStore,
    context: EventContext,
) -> LeafRollout:
    """Score one leaf and append canonical `leaf_scored` event row.

    Args:
        adapter: lm_eval adapter used for scoring/verification.
        doc_payload: Task document payload for scoring.
        leaf: Leaf rollout to score.
        store: Artifact store for event logging.
        context: Doc-scoped event attribution context.

    Returns:
        Scored leaf rollout row.
    """

    scored_leaf = score_leaf(adapter=adapter, doc=doc_payload, leaf=leaf)
    store.append_event(
        context=context,
        event_type="leaf_scored",
        payload=leaf_scored_event_payload(leaf=scored_leaf),
    )
    return scored_leaf


def finalize_scored_leaves(
    *,
    leaves: list[LeafRollout],
    pre_scored: dict[str, LeafRollout],
    scored_during_rollout: dict[str, LeafRollout],
    adapter: LmEvalAdapter,
    doc_payload: dict[str, Any],
    store: ArtifactStore,
    context: EventContext,
) -> list[LeafRollout]:
    """Return fully-scored leaves with duplicate-score suppression.

    Args:
        leaves: Leaves returned by runtime rollout execution.
        pre_scored: Resume-replayed leaves already scored in prior events.
        scored_during_rollout: Leaves scored live on completion in this attempt.
        adapter: lm_eval adapter used for fallback scoring.
        doc_payload: Task document payload for fallback scoring.
        store: Artifact store for fallback event logging.
        context: Doc-scoped event attribution context.

    Returns:
        Ordered scored leaves for diagnostics and doc aggregation.
    """

    scored_rows: list[LeafRollout] = []
    for leaf in leaves:
        existing = pre_scored.get(leaf.leaf_id)
        if existing is not None:
            scored_rows.append(existing)
            continue
        live_scored = scored_during_rollout.get(leaf.leaf_id)
        if live_scored is not None:
            scored_rows.append(live_scored)
            continue
        scored_rows.append(
            score_leaf_and_log_event(
                adapter=adapter,
                doc_payload=doc_payload,
                leaf=leaf,
                store=store,
                context=context,
            )
        )
    return scored_rows


def score_leaf(
    *,
    adapter: LmEvalAdapter,
    doc: dict[str, Any],
    leaf: LeafRollout,
) -> LeafRollout:
    """Score one leaf with lm_eval and verification bridge.

    Args:
        adapter: lm_eval adapter.
        doc: Task document payload.
        leaf: Leaf rollout.

    Returns:
        Scored leaf rollout.
    """

    task_metrics = adapter.score_response(doc=doc, response_text=leaf.text)
    verification = adapter.verification(doc=doc, response_text=leaf.text)
    return replace(leaf, task_metrics=task_metrics, verification=verification)


def leaf_scored_event_payload(*, leaf: LeafRollout) -> dict[str, Any]:
    """Serialize one scored leaf payload for event log replay.

    Args:
        leaf: Scored leaf rollout.

    Returns:
        JSON-ready leaf payload.
    """

    return {
        "leaf_id": leaf.leaf_id,
        "node_id": leaf.node_id,
        "text": leaf.text,
        "token_ids": list(leaf.token_ids),
        "tokens": [asdict(token) for token in leaf.tokens],
        "verification": leaf.verification,
        "length_tokens_total": leaf.length_tokens_total,
        "length_tokens_exec": leaf.length_tokens_exec,
        "stop_reason": leaf.stop_reason,
        "task_metrics": leaf.task_metrics,
    }


def diagnostics_from_payload(
    *,
    payload: dict[str, Any],
    doc_id: int,
    selector_mode: str,
) -> DocDiagnostics:
    """Parse doc diagnostics payload from event log into typed dataclass.

    Args:
        payload: Diagnostics payload from `doc_finished`.
        doc_id: Document id fallback.
        selector_mode: Selector mode fallback.

    Returns:
        Parsed diagnostics dataclass.
    """

    length_payload = payload.get("length_summary", {})
    breakpoint_payload = payload.get("breakpoint_variance", {})
    return DocDiagnostics(
        doc_id=int(payload.get("doc_id", doc_id)),
        selector_mode=str(payload.get("selector_mode", selector_mode)),
        verification_variance_leaf=float(
            payload.get("verification_variance_leaf", 0.0)
        ),
        length_variance_leaf=float(payload.get("length_variance_leaf", 0.0)),
        breakpoint_variance=BreakpointVariance(
            bp1_verification_unweighted=float(
                breakpoint_payload.get("bp1_verification_unweighted", 0.0)
            ),
            bp1_verification_weighted=float(
                breakpoint_payload.get("bp1_verification_weighted", 0.0)
            ),
            bp1_length_unweighted=float(
                breakpoint_payload.get("bp1_length_unweighted", 0.0)
            ),
            bp1_length_weighted=float(
                breakpoint_payload.get("bp1_length_weighted", 0.0)
            ),
            bp2_verification_unweighted=float(
                breakpoint_payload.get("bp2_verification_unweighted", 0.0)
            ),
            bp2_verification_weighted=float(
                breakpoint_payload.get("bp2_verification_weighted", 0.0)
            ),
            bp2_length_unweighted=float(
                breakpoint_payload.get("bp2_length_unweighted", 0.0)
            ),
            bp2_length_weighted=float(
                breakpoint_payload.get("bp2_length_weighted", 0.0)
            ),
        ),
        length_summary=LengthSummary(
            count=int(length_payload.get("count", 0)),
            mean_value=float(length_payload.get("mean_value", 0.0)),
            median_value=float(length_payload.get("median_value", 0.0)),
            std_value=float(length_payload.get("std_value", 0.0)),
        ),
    )


