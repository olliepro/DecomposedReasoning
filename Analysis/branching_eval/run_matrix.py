@dataclass(frozen=True)
class DocRunResult:
    """One document rollout result after scoring and diagnostics.

    Args:
        doc_id: Document id.
        doc_attempt: Attempt index.
        scored_leaves: Scored leaves for this document.
        doc_metrics: Reduced per-doc task metrics.
        diagnostics: Per-doc diagnostics payload.

    Returns:
        Dataclass containing finalized doc-level outputs.
    """

    doc_id: int
    doc_attempt: int
    scored_leaves: list[LeafRollout]
    doc_metrics: dict[str, Any]
    diagnostics: DocDiagnostics


@dataclass(frozen=True)
class DocExecutionPlan:
    """Execution plan row for one doc attempt.

    Args:
        doc_record: Doc payload from lm_eval adapter.
        doc_attempt: Attempt index to execute.
        resumed_reason: Resume reason when continuing a prior incomplete doc.
        resume_in_place: Whether this plan continues an existing attempt index.
        has_started_event: Whether this attempt already emitted doc-start lifecycle.

    Returns:
        Dataclass for per-doc execution scheduling.
    """

    doc_record: DocRecord
    doc_attempt: int
    resumed_reason: str | None
    resume_in_place: bool
    has_started_event: bool


def seed_for_doc(*, base_seed: int, doc_id: int) -> int:
    """Derive a deterministic, doc-specific RNG seed.

    Args:
        base_seed: Experiment-level seed from the run matrix.
        doc_id: Document id.

    Returns:
        Non-negative 31-bit seed unique per `(base_seed, doc_id)` pair.

    Example:
        >>> seed_for_doc(base_seed=1234, doc_id=0) != seed_for_doc(base_seed=1234, doc_id=1)
        True
    """

    mixed_seed = (base_seed * 1_000_003) ^ (doc_id * 2_654_435_761)
    return mixed_seed & 0x7FFF_FFFF


def requested_selectors_for_spec(
    *, configured_selectors: tuple[SelectorMode, ...], active_selector: SelectorMode
) -> tuple[SelectorMode, ...]:
    """Return deduped selector modes required for one branching spec.

    Args:
        configured_selectors: Selector modes configured for shared pool logging.
        active_selector: Selector mode used to expand the current branching run.

    Returns:
        Ordered selector tuple including configured modes and active selector.

    Example:
        >>> requested_selectors_for_spec(configured_selectors=("random",), active_selector="cluster_across")
        ('random', 'cluster_across')
    """

    ordered: list[SelectorMode] = []
    for mode in configured_selectors + (active_selector,):
        if mode in ordered:
            continue
        ordered.append(mode)
    return tuple(ordered)


def run_experiment_matrix(
    *,
    config: BranchingEvalConfig,
    limit: int | None,
    seed_override: int | None,
    selector_override: SelectorMode | None,
    model_override: str | None,
    doc_ids: tuple[int, ...] | None = None,
    resume_run_dir: Path | None = None,
) -> list[Path]:
    """Run expanded experiment matrix and write artifacts.

    Args:
        config: Branching-eval root config.
        limit: Optional doc cap override.
        doc_ids: Optional explicit doc ids to execute.
        seed_override: Optional seed override.
        selector_override: Optional selector override for branching runs.
        model_override: Optional model-id override.
        resume_run_dir: Existing run dir for doc-level resume.

    Returns:
        List of run directories generated by this execution.
    """

    experiments = expand_experiments(
        config=config,
        seed_override=seed_override,
        selector_override=selector_override,
        model_override=model_override,
    )
    assert experiments, "No experiments selected for the provided overrides"
    resume_target = resume_run_dir.resolve() if resume_run_dir is not None else None
    if resume_target is not None:
        assert (
            len(experiments) == 1
        ), "--resume-run-dir requires one concrete experiment after overrides"
    run_dirs: list[Path] = []
    for model_index, model_spec in enumerate(config.models):
        model_experiments = [
            spec for spec in experiments if spec.model_id == model_spec.model_id
        ]
        if not model_experiments:
            continue
        port = config.serve.base_port + model_index
        serve_log_dir = config.artifacts.output_root / "serve_logs"
        with managed_vllm_server(
            model_spec=model_spec,
            serve_config=config.serve,
            port=port,
            log_dir=serve_log_dir,
        ) as running_server:
            client = VllmClient(base_url=running_server.base_url)
            for spec in model_experiments:
                run_dir = run_one_experiment(
                    config=config,
                    model_spec=model_spec,
                    spec=spec,
                    client=client,
                    model_name_for_generation=running_server.model_name_for_generation,
                    limit=limit,
                    doc_ids=doc_ids,
                    resume_run_dir=resume_target,
                )
                run_dirs.append(run_dir)
                if resume_target is not None:
                    resume_target = None
    return run_dirs


def expand_experiments(
    *,
    config: BranchingEvalConfig,
    seed_override: int | None,
    selector_override: SelectorMode | None,
    model_override: str | None,
) -> list[ExperimentSpec]:
    """Expand config into concrete experiment specs.

    Args:
        config: Root branching-eval config.
        seed_override: Optional seed override.
        selector_override: Optional selector override.
        model_override: Optional model-id override.

    Returns:
        Ordered list of concrete experiment specs.
    """

    selectors = (
        (selector_override,)
        if selector_override is not None
        else config.run_matrix.selectors
    )
    seeds = (
        (seed_override,) if seed_override is not None else config.run_matrix.seed_values
    )
    experiments: list[ExperimentSpec] = []
    for task_name in config.tasks.task_names:
        for model_spec in config.models:
            if model_override is not None and model_spec.model_id != model_override:
                continue
            for seed in seeds:
                if config.run_matrix.include_baselines:
                    experiments.append(
                        ExperimentSpec(
                            task_name=task_name,
                            model_id=model_spec.model_id,
                            mode="baseline",
                            selector=None,
                            seed=seed,
                            baseline_rollouts=config.run_matrix.baseline_rollouts,
                            trigger_steer=False,
                            trigger_entropy=False,
                        )
                    )
                if not config.run_matrix.include_branching:
                    continue
                for selector in selectors:
                    experiments.append(
                        ExperimentSpec(
                            task_name=task_name,
                            model_id=model_spec.model_id,
                            mode="branching",
                            selector=selector,
                            seed=seed,
                            baseline_rollouts=config.run_matrix.baseline_rollouts,
                            trigger_steer=model_spec.trigger_steer_default,
                            trigger_entropy=model_spec.trigger_entropy_default,
                        )
                    )
    return experiments


def filter_docs_by_ids(
    *, docs: list[DocRecord], doc_ids: tuple[int, ...] | None
) -> list[DocRecord]:
    """Filter docs to an explicit list of document ids.

    Args:
        docs: Ordered docs loaded from one lm_eval task.
        doc_ids: Optional explicit doc ids to run.

    Returns:
        Ordered docs restricted to the requested ids.

    Example:
        >>> filter_docs_by_ids(
        ...     docs=[DocRecord(doc_id=0, doc_payload={}, prompt_text="p0")],
        ...     doc_ids=(0,),
        ... )[0].doc_id
        0
    """

    if doc_ids is None:
        return docs
    requested_doc_ids = set(doc_ids)
    assert all(doc_id >= 0 for doc_id in requested_doc_ids), "doc ids must be >= 0"
    filtered_docs = [doc for doc in docs if doc.doc_id in requested_doc_ids]
    available_doc_ids = {doc.doc_id for doc in filtered_docs}
    missing_doc_ids = sorted(requested_doc_ids - available_doc_ids)
    assert not missing_doc_ids, (
        "requested doc ids are unavailable after limit/default_limit: "
        f"{missing_doc_ids}"
    )
    return filtered_docs


def run_one_experiment(
    *,
    config: BranchingEvalConfig,
    model_spec: ModelSpec,
    spec: ExperimentSpec,
    client: VllmClient,
    model_name_for_generation: str,
    limit: int | None,
    doc_ids: tuple[int, ...] | None,
    resume_run_dir: Path | None,
) -> Path:
    """Run one concrete experiment and persist outputs.

    Args:
        config: Root config.
        model_spec: Model specification.
        spec: Concrete experiment spec.
        client: Connected vLLM client.
        model_name_for_generation: Request-time model name.
        limit: Optional doc cap.
        doc_ids: Optional explicit doc ids to execute.
        resume_run_dir: Existing run directory for resume.

    Returns:
        Run directory path for this experiment.
    """

    run_dir = (
        resume_run_dir.resolve()
        if resume_run_dir is not None
        else build_run_dir(
            config=config,
            spec=spec,
        )
    )
    store = ArtifactStore(
        run_dir=run_dir,
        reuse_candidate_pools=config.artifacts.reuse_candidate_pools,
        run_id=run_dir.name,
    )
    config_snapshot = build_config_snapshot(
        config=config,
        spec=spec,
        model_spec=model_spec,
    )
    _initialize_or_validate_run_metadata(
        store=store,
        spec=spec,
        config_snapshot=config_snapshot,
        resume_run_dir=resume_run_dir,
    )
    adapter = LmEvalAdapter(task_name=spec.task_name)
    docs = adapter.docs(
        limit=limit if limit is not None else config.run_matrix.default_limit
    )
    docs = filter_docs_by_ids(docs=docs, doc_ids=doc_ids)
    events_before = store.read_events()
    plans, skipped_doc_count = build_doc_execution_plans(
        docs=docs,
        existing_events=events_before,
        resume_enabled=resume_run_dir is not None,
    )
    run_context = run_event_context(spec=spec, run_id=store.run_id)
    store.append_event(
        context=run_context,
        event_type="run_started",
        payload={
            "resume": resume_run_dir is not None,
            "resume_run_dir": (
                str(resume_run_dir) if resume_run_dir is not None else None
            ),
            "doc_count_total": len(docs),
            "doc_count_scheduled": len(plans),
            "doc_count_skipped": skipped_doc_count,
            "mode": spec.mode,
            "doc_ids": list(doc_ids) if doc_ids is not None else None,
        },
    )
    resolved_branching = resolve_branching_config(
        branching=config.branching,
        calibration_path=config.calibration.entropy_threshold_path,
    )
    asyncio.run(
        run_docs_with_limit_async(
            plans=plans,
            max_concurrent_docs=MAX_CONCURRENT_DOC_ROLLOUTS,
            config=config,
            spec=spec,
            client=client,
            model_name_for_generation=model_name_for_generation,
            task_name=spec.task_name,
            model_id=spec.model_id,
            resolved_branching=resolved_branching,
            store=store,
            adapter=adapter,
            events_snapshot=events_before,
        )
    )
    summary = recompute_outputs_from_events(
        store=store,
        adapter=adapter,
        selector_mode=spec.selector or "baseline",
    )
    store.append_event(
        context=run_context,
        event_type="run_finished",
        payload={
            "resume": resume_run_dir is not None,
            "doc_count_total": len(docs),
            "doc_count_scheduled": len(plans),
            "doc_count_skipped": skipped_doc_count,
            **summary,
        },
    )
    return run_dir


def _initialize_or_validate_run_metadata(
    *,
    store: ArtifactStore,
    spec: ExperimentSpec,
    config_snapshot: dict[str, Any],
    resume_run_dir: Path | None,
) -> None:
    if resume_run_dir is None:
        store.write_config_snapshot(payload=config_snapshot)
        store.write_run_manifest(
            payload=build_run_manifest_payload(
                run_id=store.run_id,
                task_name=spec.task_name,
                model_id=spec.model_id,
                selector_mode=spec.selector or "baseline",
                config_snapshot=config_snapshot,
                git_commit=read_git_commit_hash(),
            )
        )
        return
    existing_manifest = store.load_run_manifest()
    existing_snapshot = store.load_config_snapshot()
    assert existing_manifest is not None, "resume run is missing run_manifest.json"
    assert existing_snapshot is not None, "resume run is missing config_snapshot.json"
    assert (
        str(existing_manifest.get("task_name", "")) == spec.task_name
    ), "resume mismatch: task_name differs from existing run manifest"
    assert (
        str(existing_manifest.get("model_id", "")) == spec.model_id
    ), "resume mismatch: model_id differs from existing run manifest"
    expected_selector = spec.selector or "baseline"
    assert (
        str(existing_manifest.get("selector_mode", "")) == expected_selector
    ), "resume mismatch: selector_mode differs from existing run manifest"
    assert (
        existing_snapshot == config_snapshot
    ), "resume mismatch: config snapshot changed"


def build_doc_execution_plans(
    *,
    docs: list[DocRecord],
    existing_events: list[Any],
    resume_enabled: bool,
) -> tuple[list[DocExecutionPlan], int]:
    """Build doc execution plans from resume state and event history.

    Args:
        docs: Ordered doc rows.
        existing_events: Parsed event envelope rows.
        resume_enabled: Whether this invocation is resuming an existing run.

    Returns:
        Tuple `(plans, skipped_doc_count)`.
    """

    if not resume_enabled:
        plans = [
            DocExecutionPlan(
                doc_record=doc_record,
                doc_attempt=0,
                resumed_reason=None,
                resume_in_place=False,
                has_started_event=False,
            )
            for doc_record in docs
        ]
        return plans, 0
    max_attempt_by_doc: dict[int, int] = {}
    latest_finished_attempt_by_doc: dict[int, int] = {}
    started_attempts_by_doc: dict[int, set[int]] = {}
    for event in existing_events:
        if event.doc_id is None or event.doc_attempt is None:
            continue
        max_attempt_by_doc[event.doc_id] = max(
            max_attempt_by_doc.get(event.doc_id, -1),
            event.doc_attempt,
        )
        if event.event_type in {"doc_started", "doc_resumed"}:
            started_attempts_by_doc.setdefault(event.doc_id, set()).add(
                event.doc_attempt
            )
        if event.event_type != "doc_finished":
            continue
        latest_finished_attempt_by_doc[event.doc_id] = max(
            latest_finished_attempt_by_doc.get(event.doc_id, -1),
            event.doc_attempt,
        )
    plans: list[DocExecutionPlan] = []
    skipped_doc_count = 0
    for doc_record in docs:
        doc_id = doc_record.doc_id
        latest_seen_attempt = max_attempt_by_doc.get(doc_id, -1)
        latest_finished_attempt = latest_finished_attempt_by_doc.get(doc_id)
        if (
            latest_finished_attempt is not None
            and latest_finished_attempt == latest_seen_attempt
        ):
            skipped_doc_count += 1
            continue
        has_started_event = latest_seen_attempt in started_attempts_by_doc.get(
            doc_id, set()
        )
        if latest_seen_attempt >= 0:
            next_attempt = latest_seen_attempt
            resumed_reason = "resume_from_partial_logs"
            resume_in_place = True
        else:
            next_attempt = 0
            resumed_reason = None
            resume_in_place = False
        plans.append(
            DocExecutionPlan(
                doc_record=doc_record,
                doc_attempt=next_attempt,
                resumed_reason=resumed_reason,
                resume_in_place=resume_in_place,
                has_started_event=has_started_event,
            )
        )
    return plans, skipped_doc_count


def run_doc_rollouts(
    *,
    config: BranchingEvalConfig,
    spec: ExperimentSpec,
    client: VllmClient,
    model_name_for_generation: str,
    prompt_text: str,
    doc_id: int,
    doc_attempt: int,
    task_name: str,
    model_id: str,
    resolved_branching: BranchingConfig,
    store: ArtifactStore,
    resume_state: BranchResumeState | None = None,
    on_leaf_completed: LeafCompletionHook | None = None,
) -> tuple[list[LeafRollout], BranchTree | None]:
    """Run baseline or branching rollouts for one doc.

    Args:
        config: Root config.
        spec: Experiment spec.
        client: vLLM client.
        model_name_for_generation: Request-time model name.
        prompt_text: Prompt string.
        doc_id: Document id.
        doc_attempt: Attempt index.
        task_name: Task name.
        model_id: Model label.
        resolved_branching: Branching config with threshold resolved.
        store: Artifact store.
        resume_state: Optional partial-attempt replay state from event logs.
        on_leaf_completed: Optional callback invoked when one leaf completes.

    Returns:
        Tuple `(leaves, optional_tree)`.
    """

    env_paths = default_env_paths()
    selector_mode = spec.selector or "baseline"
    active_selector: SelectorMode = spec.selector or "random"
    executor = BranchExecutor(
        client=client,
        prompt_text=prompt_text,
        model_name=model_name_for_generation,
        decoding=config.decoding,
        branching=resolved_branching,
        artifact_store=store,
        requested_selectors=requested_selectors_for_spec(
            configured_selectors=config.run_matrix.selectors,
            active_selector=active_selector,
        ),
        active_selector=active_selector,
        on_leaf_completed=on_leaf_completed,
        seed=seed_for_doc(base_seed=spec.seed, doc_id=doc_id),
        trigger_steer_enabled=spec.trigger_steer,
        trigger_entropy_enabled=spec.trigger_entropy,
        env_paths=env_paths,
        cluster_cache_path=store.cache_dir / "cluster_prompt_cache.json",
        embedding_cache_path=store.cache_dir / "embedding_cache.json",
        enable_request_priorities=config.serve.scheduling_policy == "priority",
    )
    executor.set_event_context(
        doc_id=doc_id,
        doc_attempt=doc_attempt,
        task_name=task_name,
        model_id=model_id,
        selector_mode=selector_mode,
    )
    if resume_state is not None:
        executor.restore_request_stream_state(
            request_stream_state=resume_state.request_stream_state
        )
    if spec.mode == "baseline":
        assert resume_state is None, "baseline resume replay is not implemented"
        leaves = executor.run_standard_rollouts(rollout_count=spec.baseline_rollouts)
        return leaves, None
    if (
        resume_state is not None
        and resume_state.rollout_finished_seen
        and not resume_state.frontier
    ):
        return list(resume_state.tree.leaves), resume_state.tree
    if resume_state is not None:
        tree = executor.run_branching_rollouts_from_frontier(
            doc_id=doc_id,
            doc_attempt=doc_attempt,
            task_name=task_name,
            model_id=model_id,
            tree=resume_state.tree,
            frontier=list(resume_state.frontier),
        )
        return list(tree.leaves), tree
    tree = executor.run_branching_rollouts(
        doc_id=doc_id,
        doc_attempt=doc_attempt,
        task_name=task_name,
        model_id=model_id,
    )
    return list(tree.leaves), tree


