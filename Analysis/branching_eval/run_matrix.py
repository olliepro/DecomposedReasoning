def build_doc_diagnostics(*, tree: BranchTree) -> DocDiagnostics:
    """Build per-doc diagnostics for one branching tree.

    Args:
        tree: Branch tree with scored leaves.

    Returns:
        Per-doc diagnostics dataclass.
    """

    verification_leaf_variance, length_leaf_variance = compute_leaf_variance(
        leaves=tree.leaves
    )
    breakpoint_variance = compute_breakpoint_variance(tree=tree)
    length_summary = summarize_lengths(
        lengths=[leaf.length_tokens_total for leaf in tree.leaves]
    )
    return DocDiagnostics(
        doc_id=tree.doc_id,
        selector_mode=tree.selector_mode,
        verification_variance_leaf=verification_leaf_variance,
        length_variance_leaf=length_leaf_variance,
        breakpoint_variance=breakpoint_variance,
        length_summary=length_summary,
    )


def build_baseline_doc_diagnostics(
    *,
    doc_id: int,
    leaves: list[LeafRollout],
) -> DocDiagnostics:
    """Build per-doc diagnostics for a baseline run.

    Args:
        doc_id: Document id.
        leaves: Baseline leaves.

    Returns:
        Per-doc diagnostics with zero breakpoint variance.
    """

    baseline_tree = BranchTree(
        doc_id=doc_id,
        task_name="baseline",
        model_id="baseline",
        selector_mode="baseline",
        root_prompt="",
    )
    baseline_tree.add_node(
        node=TreeNode(
            node_id="node_root",
            parent_node_id=None,
            prompt_text="",
            assistant_prefix="",
            prompt_token_ids=None,
            branch_points_used=0,
        )
    )
    baseline_tree.leaves = leaves
    return build_doc_diagnostics(tree=baseline_tree)


def resolve_branching_config(
    *,
    branching: BranchingConfig,
    calibration_path: Path,
) -> BranchingConfig:
    """Resolve entropy threshold from config or calibration JSON.

    Args:
        branching: Branching config.
        calibration_path: Calibration JSON path.

    Returns:
        Branching config with entropy threshold set.
    """

    if branching.entropy_threshold is not None:
        return branching
    payload = json.loads(calibration_path.read_text(encoding="utf-8"))
    assert isinstance(payload, dict), "calibration payload must be a mapping"
    profile = payload.get(branching.entropy_profile_name)
    assert isinstance(profile, dict), "calibration profile missing"
    threshold = profile.get("entropy_threshold")
    assert isinstance(threshold, (int, float)), "entropy_threshold missing in profile"
    return replace(branching, entropy_threshold=float(threshold))


def build_run_dir(*, config: BranchingEvalConfig, spec: ExperimentSpec) -> Path:
    """Build deterministic experiment run directory path.

    Args:
        config: Root config.
        spec: Experiment spec.

    Returns:
        Run directory path.
    """

    timestamp = datetime.now(tz=timezone.utc).strftime("%Y%m%dT%H%M%S.%fZ")
    selector_label = spec.selector if spec.selector is not None else "baseline"
    run_name = (
        f"{spec.task_name}_{spec.model_id}_{spec.mode}_{selector_label}"
        f"_seed{spec.seed}_{timestamp}"
    )
    return (config.artifacts.output_root / run_name).resolve()


