def build_length_diagnostics(*, lengths: list[int]) -> dict[str, Any]:
    """Build length summary payload from all leaf lengths.

    Args:
        lengths: Leaf total lengths across docs.

    Returns:
        Length diagnostics mapping.
    """

    if not lengths:
        return {"count": 0, "mean": 0.0, "median": 0.0, "std": 0.0}
    average = float(mean(lengths))
    variance = sum((value - average) ** 2 for value in lengths) / len(lengths)
    return {
        "count": len(lengths),
        "mean": average,
        "median": float(median(lengths)),
        "std": float(variance**0.5),
    }


def read_git_commit_hash() -> str:
    """Read current git commit hash.

    Args:
        None.

    Returns:
        Commit hash string, or `unknown` when unavailable.
    """

    try:
        result = subprocess.run(
            ["git", "rev-parse", "HEAD"],
            check=True,
            capture_output=True,
            text=True,
        )
    except subprocess.CalledProcessError:
        return "unknown"
    return result.stdout.strip() or "unknown"


