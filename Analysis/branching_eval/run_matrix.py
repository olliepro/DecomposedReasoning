def _json_ready(*, value: Any) -> Any:
    if isinstance(value, Path):
        return str(value)
    if isinstance(value, dict):
        return {str(key): _json_ready(value=item) for key, item in value.items()}
    if isinstance(value, list):
        return [_json_ready(value=item) for item in value]
    if isinstance(value, tuple):
        return [_json_ready(value=item) for item in value]
    return value
